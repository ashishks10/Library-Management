    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String BookID = jTextField1.getText();
        String StudentRollNo = jTextField2.getText();
        try
        {
            Connection conn=ConnectionProvider.getConn();
            Statement s=conn.createStatement();
            ResultSet result= s.executeQuery("select * from issuedBooks where bookID='"+BookID+"' and studentID='"+StudentRollNo+"' and returnStatus='false'");
            if(result.next())
            {
                String IssueDate = result.getString(3);
                String str = IssueDate.substring(6,10)+IssueDate.substring(3,5)+IssueDate.substring(0,2);
                DateTimeFormatter inputFormat = DateTimeFormatter.ofPattern("yyyyMMdd");
                LocalDate dateTime = LocalDate.parse(str, inputFormat);
                String DueD = (dateTime.plusDays(30)).toString();
                String DueDate = DueD.substring(8,10)+"-"+DueD.substring(5,7)+"-"+DueD.substring(0,4);
                jTextField5.setText(IssueDate);
                jTextField4.setText(DueDate);
                jTextField1.setEditable(false);
                jTextField2.setEditable(false);
                jTextField5.setEditable(false);
                jTextField4.setEditable(false);
                check=1;
            }
            else 
            {
                JOptionPane.showMessageDialog(null,"This Record Not Found");
                check=0;
                setVisible(false);
                new ReturnBook().setVisible(true);
            }
                
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Error");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

