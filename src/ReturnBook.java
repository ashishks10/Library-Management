    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(check==0)
        {
            JOptionPane.showMessageDialog(null,"First Get Details, then Return.");
            return;
        }
        String BookID = jTextField1.getText();
        String StudentRollNo = jTextField2.getText();
        SimpleDateFormat DateFormat= new SimpleDateFormat("dd-MM-yyyy");
        String ReturnDate = DateFormat.format(jDateChooser1.getDate());
        try
        {
            Connection conn=ConnectionProvider.getConn();
            Statement s=conn.createStatement();
            ResultSet result= s.executeQuery("select * from issuedBooks where bookID='"+BookID+"' and studentID='"+StudentRollNo+"'");
            if(result.next() && check==1)
            {
                String DueDate = jTextField4.getText();
                String DDate = DueDate.substring(6,10)+DueDate.substring(3,5)+DueDate.substring(0,2);
                String RDate = ReturnDate.substring(6,10)+ReturnDate.substring(3,5)+ReturnDate.substring(0,2);
                DateTimeFormatter inputFormat = DateTimeFormatter.ofPattern("yyyyMMdd");
                LocalDate DueD = LocalDate.parse(DDate, inputFormat);
                LocalDate ReturnD = LocalDate.parse(RDate, inputFormat);
                int fine=(int)DueD.until(ReturnD, ChronoUnit.DAYS);
                s.executeUpdate("delete from issuedBooks where bookID ='"+BookID+"' and studentID='"+StudentRollNo+"'");
                s.executeUpdate("update books set quantity = quantity +1 where bookID='"+BookID+"'");
                if(fine>0){
                    JOptionPane.showMessageDialog(null,"Book Returned\nFine : Rs. "+fine+".0");
                }
                else 
                {
                    JOptionPane.showMessageDialog(null,"Book Returned");
                }
                setVisible(false);
                new ReturnBook().setVisible(true);
            }
            else 
            {
                JOptionPane.showMessageDialog(null,"This Record Not Found");

            }
                
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,"Error");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

